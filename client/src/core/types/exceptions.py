# client/src/core/types/exceptions.py
"""
БАЗОВЫЕ ИСКЛЮЧЕНИЯ ЯДРА.

Иерархия исключений для всех компонентов core.
Все исключения в приложении должны наследоваться от этих базовых классов.

КЛЮЧЕВЫЕ ПРИНЦИПЫ:
- Исключения не содержат логики (логирование — в обработчиках)
- Чёткая иерархия для разных типов ошибок
- Поддержка дополнительных деталей для отладки

ПРАВИЛА:
- Все исключения core наследуются от CoreError
- В обработчиках можно ловить CoreError для всех ошибок ядра
- Детали передаются в конструктор, но не логируются автоматически
"""
from typing import Optional


class CoreError(Exception):
    """
    Базовое исключение для всех ошибок ядра.
    
    Все исключения в core должны наследоваться от этого класса.
    Это позволяет отлавливать все ошибки ядра одним except.
    
    Атрибуты:
        message: Сообщение об ошибке
        details: Дополнительные детали (для отладки)
    """
    
    def __init__(self, message: str, details: Optional[dict] = None):
        self.message = message
        self.details = details or {}
        super().__init__(message)


class ConfigurationError(CoreError):
    """
    Ошибка конфигурации (неправильные настройки).
    
    Возникает, когда:
    - Не указана обязательная переменная окружения
    - Некорректный формат конфигурации
    - Неверный путь к файлу
    """
    pass


class HierarchyError(CoreError):
    """
    Ошибка в иерархии объектов (неправильные связи).
    
    Возникает, когда:
    - Попытка установить недопустимую связь родитель-потомок
    - Циклическая зависимость в иерархии
    - Нарушение правил иерархии из schema.py
    """
    pass


class SerializationError(CoreError):
    """
    Ошибка сериализации/десериализации.
    
    Возникает, когда:
    - Невозможно распарсить строковый ключ (key_to_identifier)
    - Некорректный формат display (parse_display)
    - Неизвестный тип узла в ключе
    - Некорректный ID в ключе
    
    Пример:
        >>> key_to_identifier('invalid:key')
        SerializationError: Неверный формат ключа 'invalid:key'
    """
    pass


class ValidationError(CoreError):
    """
    Ошибка валидации данных.
    
    Возникает, когда:
    - Передан некорректный ID (validate_positive_int)
    - Пустая строка там, где должно быть значение (validate_non_empty)
    - Неизвестный тип узла (validate_node_type)
    """
    pass


class NotFoundError(CoreError):
    """
    Объект не найден.
    
    Возникает, когда:
    - Запрошенный узел отсутствует в графе (repository.get)
    - API вернул 404
    - Объект не существует в системе
    """
    pass


class DuplicateError(CoreError):
    """
    Дубликат объекта.
    
    Возникает, когда:
    - Попытка добавить объект, который уже существует
    - Конфликт идентификаторов
    """
    pass


class ConnectionError(CoreError):
    """
    Ошибка соединения с сервером.
    
    Возникает, когда:
    - Нет доступа к API
    - Таймаут при запросе
    - Сетевая ошибка
    """
    pass


class ApiError(CoreError):
    """
    Ошибка при обращении к API.
    
    Возникает, когда:
    - Сервер вернул HTTP ошибку (4xx, 5xx)
    - Некорректный ответ от сервера
    """
    pass