# client/src/core/types/nodes.py
"""
ТИПЫ УЗЛОВ.

Содержит только определения, связанные с узлами иерархии:
- Перечисление типов узлов
- Псевдонимы для идентификаторов
- Value Object для идентификатора узла

Никакой логики, только данные!
"""
from enum import Enum
from typing import Tuple
from dataclasses import dataclass


class NodeType(str, Enum):
    """
    Типы узлов в иерархии объектов недвижимости.
    
    Это основное перечисление, определяющее все возможные типы
    сущностей в системе. Используется для идентификации типа узла
    в дереве, определения связей, фильтрации событий.
    
    Значения:
        COMPLEX: Комплекс зданий (корневой уровень)
        BUILDING: Корпус в составе комплекса
        FLOOR: Этаж в составе корпуса
        ROOM: Помещение на этаже
        COUNTERPARTY: Контрагент (юридическое лицо)
        RESPONSIBLE_PERSON: Ответственное лицо (контакт)
    """
    COMPLEX = "complex"
    """Комплекс зданий (корневой уровень иерархии)"""
    
    BUILDING = "building"
    """Корпус в составе комплекса"""
    
    FLOOR = "floor"
    """Этаж в составе корпуса (может быть отрицательным для подвалов)"""
    
    ROOM = "room"
    """Помещение на этаже"""
    
    COUNTERPARTY = "counterparty"
    """Контрагент (юридическое лицо, владелец, арендатор)"""
    
    RESPONSIBLE_PERSON = "responsible_person"
    """Ответственное лицо (контактное лицо контрагента)"""
    
    def __str__(self) -> str:
        """Возвращает строковое представление типа."""
        return self.value


# Псевдонимы типов для читаемости кода
NodeID = int
"""Уникальный идентификатор узла в системе (числовой)."""

NodeKey = str
"""
Ключ для быстрого доступа к узлу в индексах.
Формат: "{тип}:{id}", например "complex:42" или "building:101"
"""

ParentInfo = Tuple[NodeType, NodeID]
"""
Информация о родительском узле для навигации по иерархии.
Используется для построения контекста и перемещения вверх по дереву.
"""


@dataclass(frozen=True)
class NodeIdentifier:
    """
    Структурированный идентификатор узла (Value Object).
    
    В отличие от строкового ключа, этот формат безопасен по типам
    и используется внутри приложения для передачи идентификаторов.
    
    Атрибуты:
        node_type: Тип узла (из перечисления NodeType)
        node_id: Числовой идентификатор узла
        
    Пример:
        >>> id = NodeIdentifier(NodeType.COMPLEX, 42)
        >>> id.node_type
        <NodeType.COMPLEX: 'complex'>
        >>> id.node_id
        42
        
    Важно:
        Этот класс не содержит логики сериализации!
        Все преобразования вынесены в core.serializers.
    """
    node_type: NodeType
    node_id: NodeID
    
    def __post_init__(self):
        """Валидация после инициализации (только для отладки)."""
        from utils.logger import get_logger
        log = get_logger(__name__)
        log.debug(f"✅ Создан NodeIdentifier: {self}")